#include <stdio.h> 

int main() {
    // Khai bao va khoi tao hai bien so nguyen 
    
	// Bien so nguyen thu nhat
	int num1 = 12; 
    
    // Bien so nguyen thu hai
	int num2 = 4; 

    // Tinh tong hai so va luu ket qua vao bien khac 
    int total = num1 + num2;

    // Ket qua 
    printf("Tong: %d + %d = %d", num1, num2, total);

    return 0;
}

